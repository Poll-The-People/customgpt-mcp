def main():
    """Main setup function."""
    print_header()

    # Check system requirements
    check_python_version()

    if not check_dependencies():
        print("\nâŒ Missing required dependencies. Please install them and run setup again.")
        sys.exit(1)

    # Setup steps
    steps = [
        ("ðŸ”¨ Virtual Environment", setup_virtual_environment),
        ("ðŸ“¦ Dependencies", install_dependencies),
        ("ðŸ”§ Configuration", create_env_file),
        ("ðŸ§ª Server Test", test_server)
    ]

    for step_name, step_func in steps:
        print(f"\n{step_name}")
        print("-" * 30)
        if not step_func():
            print(f"\nâŒ Setup failed at: {step_name}")
            sys.exit(1)

    print_next_steps()

if __name__ == "__main__":
    from datetime import datetime
